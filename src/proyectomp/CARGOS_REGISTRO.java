    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        if(txtNombre.getText().length()==0 || 
                this.sexo.getSelectedItem().equals(0)||
                this.TxtSue.getText().length()==0||
                this.txtFun.getText().length()==0){
            this.lblId.setText("TODO");
        }else{
            Sentencias s = new Sentencias();
            s.setNombre_ca(this.txtNombre.getText());
            s.setSueldos_ca(Double.parseDouble(this.TxtSue.getText()));
            s.setFunciones_ca(this.txtFun.getText());
            s.setTipo_ca(this.sexo.getSelectedItem().toString());
            
            
            if(Funciones.isRegister(s)){
               // Funciones.setListar("");
            JOptionPane.showMessageDialog(this, "Nuevo Usuario creado");
            Funciones.setListar("");
            
            }else{
                JOptionPane.showMessageDialog(this, "Fallo");
                
            }
            dispose();
            
            
        }
                
                
    }//GEN-LAST:event_btnGuardarActionPerformed

